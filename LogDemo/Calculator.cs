namespace UtilityKnife
{
	public class Calculator
	{
		public double Add(double x, double y)
		{
			return x + y;
		}
		public double Subtract(double x,double y)
		{
			return x - y;
		}
		public double Multiply(double x,double y)
		{
			return x * y;
		}
		public int Division(int x,int y)
		{
			return x / y;
		}
	}

}